   public static boolean estaCerrado(Object obj, JDesktopPane escritorio){
       JInternalFrame[] actives = escritorio.getAllFrames();
       boolean cerrado= true;
       int i=0;
       while(i<actives.length && cerrado){
       if(actives[i]==obj){
       cerrado = false;
       }
       i++;
       }
   return cerrado;
   }
    
    

